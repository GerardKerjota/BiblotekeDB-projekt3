    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
       
        //Ky frame sherben per te krijuar nje tabele dhe nepermjet radiobutton te trg se duam te shofim lb Artistike Enciklopedi apo Fjalor
        //duke i lexu kta lloje lb nga nje txt file
            String filePath="La.txt";
            File file=new File(filePath);
            
            String lines;
            
        if(jRadioButton1.isSelected()){   
        try {
            BufferedReader br = new BufferedReader(new FileReader(file));//lexijme txt file ku kemi shkruar te dhenat
            
            clearTable();//thrrasim funks qe kemi krijuar me siper te tipit void
            
            while((lines = br.readLine()) != null){
                String[] dataArr=lines.split("/"); //i japim info prg si te identifikoje datas
                
                if(dataArr[0].equals("Literature-Artistike")){
                    Object[] row= dataArr;//datat i ruan ne nje array

                    DefaultTableModel model= (DefaultTableModel)jTable1.getModel();
                    model.addRow(row);//krijon rreshhtat me data
                }
                 
            }
            br.close();//mbyllim skedarin txt qe ta rihapim me von per lexim
            
        } catch (Exception ex) {
            Logger.getLogger(JFrameTable.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
    }//GEN-LAST:event_jRadioButton1ActionPerformed

